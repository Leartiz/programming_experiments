package canonical

// https://leetcode.com/problems/first-missing-positive/

func FirstMissingPositive(nums []int) int {
	n := len(nums) // 4

	// Шаг 1: размещаем каждое число x в позиции x-1, если 1 <= x <= n
	for i := 0; i < n; i++ {

		// Пока текущий элемент nums[i] может быть помещён на своё место
		for {
			val := nums[i]

			// Если число вне диапазона 1..n, или уже стоит на своём месте - выходим
			if val < 1 || val > n || nums[val-1] == val {
				break
			}

			// Меняем местами nums[i] и nums[val-1]
			nums[i], nums[val-1] = nums[val-1], nums[i]
		}
	}

	/*
		Начало:          [3,  4, -1,  1]
		i=0 (3):         [-1, 4,  3,  1]   → меняем 3 и -1
		i=0 (-1): пропуск
		i=1 (4):         [-1, 1,  3,  4]   → меняем 4 и 1
		i=1 (1):         [1, -1,  3,  4]   → меняем 1 и -1
		i=1 (-1): пропуск
		i=2 (3): уже на месте
		i=3 (4): уже на месте
		Итог:            [1, -1,  3,  4]
		Проверка: индексы: 0→1, 1→??? (не 2) → ответ 2
	*/

	// Шаг 2: ищем первое отсутствующее положительное
	for i := 0; i < n; i++ {
		if nums[i] != i+1 {
			return i + 1
		}
	}

	// Если все числа от 1 до n присутствуют, ответ n+1
	return n + 1
}
